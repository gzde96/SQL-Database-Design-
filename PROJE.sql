CREATE TABLE KATILIMLAR(
KATILIM_ID SERIAL PRIMARY KEY,
KULLANICI_ID INTEGER NOT NULL,
EGITIM_ID INTEGER NOT NULL,
KATILIM_TARIHI TIMESTAMP DEFAULT CURRENT_DATE,
FOREIGN KEY (KULLANICI_ID) REFERENCES MEMBERS(KULLANICI_ID),
FOREIGN KEY (EGITIM_ID) REFERENCES EGITIMLER (EGITIM_ID)
);

CREATE TABLE SERTIFIKALAR(
SERTIFIKA_ID SERIAL PRIMARY KEY NOT NULL,
SERTIFIKA_KODU VARCHAR(100) UNIQUE NOT NULL,
VERILIS_TARIHI DATE 
);

CREATE TABLE SERTIFIKA_ATAMALARI(
ATAMA_ID SERIAL PRIMARY KEY,
KULLANICI_ID INTEGER NOT NULL,
EGITIM_ID INTEGER NOT NULL,
SERTIFIKA_ID INTEGER NOT NULL,
ALIM_TARIHI DATE,
FOREIGN KEY (KULLANICI_ID) REFERENCES MEMBERS(KULLANICI_ID),
FOREIGN KEY (EGITIM_ID) REFERENCES EGITIMLER(EGITIM_ID ),
FOREIGN KEY (SERTIFIKA_ID) REFERENCES SERTIFIKALAR(SERTIFIKA_ID)
);

CREATE TABLE BLOG_GONDERILERI(
